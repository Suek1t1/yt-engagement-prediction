import numpy as np, pandas as pd, re
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.decomposition import TruncatedSVD
RS=42
df=pd.read_csv("english_titles.csv").drop_duplicates(subset="video_id").reset_index(drop=True)
for c in ["views","likes","dislikes","comment_count"]: df[c]=pd.to_numeric(df[c],errors="coerce")
df=df[df["ratings_disabled"]!=True].reset_index(drop=True)
df=df.dropna(subset=["views","likes","dislikes","comment_count"]).reset_index(drop=True)
y=np.log1p(df["likes"].values).astype(np.float32)
def b2i(s): return s.astype(str).str.lower().isin(["true","1"]).astype(int)
feat=pd.DataFrame(index=df.index)
feat["log_views"]=np.log1p(df["views"]);feat["log_dislikes"]=np.log1p(df["dislikes"]);feat["log_comment"]=np.log1p(df["comment_count"])
feat["category_id"]=df["category_id"].astype(int)
feat["comments_disabled"]=b2i(df["comments_disabled"]);feat["video_error_or_removed"]=b2i(df["video_error_or_removed"])
feat["dislikes_per_view"]=df["dislikes"]/(df["views"]+1);feat["comment_per_view"]=df["comment_count"]/(df["views"]+1);feat["comment_per_dislike"]=df["comment_count"]/(df["dislikes"]+1)
pt=pd.to_datetime(df["publish_time"],errors="coerce");hour=pt.dt.hour.fillna(0);dow=pt.dt.dayofweek.fillna(0);month=pt.dt.month.fillna(0)
feat["pub_hour_sin"]=np.sin(2*np.pi*hour/24);feat["pub_hour_cos"]=np.cos(2*np.pi*hour/24)
feat["pub_dow_sin"]=np.sin(2*np.pi*dow/7);feat["pub_dow_cos"]=np.cos(2*np.pi*dow/7)
feat["pub_month_sin"]=np.sin(2*np.pi*month/12);feat["pub_month_cos"]=np.cos(2*np.pi*month/12)
td=pd.to_datetime(df["trending_date"],format="%y.%d.%m",errors="coerce")
feat["days_to_trend"]=(td-pt.dt.tz_localize(None)).dt.days.fillna(-1)
title=df["title"].fillna("");desc=df["description"].fillna("");tags=df["tags"].fillna("")
er=re.compile("[\U0001F000-\U0001FAFF\U00002600-\U000027BF]")
feat["title_len"]=title.str.len();feat["title_words"]=title.str.split().apply(len)
feat["title_upper_ratio"]=title.apply(lambda s:sum(c.isupper() for c in s)/(len(s)+1))
feat["title_excl"]=title.str.count("!");feat["title_quest"]=title.str.count(r"\?")
feat["title_has_digit"]=title.str.contains(r"\d").astype(int);feat["title_emoji"]=title.apply(lambda s:len(er.findall(s)))
feat["desc_len"]=desc.str.len();feat["desc_url_count"]=desc.str.count("http")
feat["tag_count"]=tags.apply(lambda t:0 if t.strip() in ("","[none]") else len(t.split("|")))
feat["log_views_x_dislikes"]=np.log1p(df["views"])*np.log1p(df["dislikes"])
feat["log_comment_x_dislikes"]=np.log1p(df["comment_count"])*np.log1p(df["dislikes"])
feat["dislike_per_comment"]=df["dislikes"]/(df["comment_count"]+1)
feat["log_views_sq"]=np.log1p(df["views"])**2
Xs=feat.astype(np.float32).values
tc=tags.apply(lambda t:t.replace("|"," ").replace('"'," "))
def sv(ts,maxf,ng,nc,an="word",cng=None):
    v=TfidfVectorizer(analyzer="char_wb",ngram_range=cng,max_features=maxf,min_df=3,sublinear_tf=True) if an=="char_wb" else TfidfVectorizer(ngram_range=ng,max_features=maxf,min_df=2,sublinear_tf=True)
    X=v.fit_transform(ts);nc=min(nc,X.shape[1]-1)
    return TruncatedSVD(n_components=nc,random_state=RS).fit_transform(X).astype(np.float32)
Xt=np.hstack([sv(title,5000,(1,2),60),sv(title,5000,None,40,"char_wb",(3,5)),sv(tc,5000,(1,2),60),sv(desc,8000,(1,2),80)]).astype(np.float32)
np.save("likes_model/Xs.npy",Xs);np.save("likes_model/Xt.npy",Xt);np.save("likes_model/y.npy",y)
df[["video_id","title","likes","channel_title"]].to_csv("likes_model/meta.csv",index=False)
import json; json.dump(list(feat.columns),open("likes_model/featnames.json","w"))
print("features built", Xs.shape, Xt.shape, flush=True)
